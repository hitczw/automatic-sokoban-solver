var.conf.int=function(x,mu=Inf,alpha){

	n=length(x)
	if(mu<Inf){
		s2=sum((x-mu)^2)/n
		df=n
	}
	else{
		s2=var(x)
		df=n-1
	}
	c(df*s2/qchisq(1-alpha/2,df),df*s2/qchisq(alpha/2,df))
}